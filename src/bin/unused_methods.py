# Data structures
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class FunctionDef:
    qualified_name: str  # e.g. "MyClass.my_method" or "my_function"
    simple_name: str  # e.g. "my_method" or "my_function"
    filename: str
    lineno: int
    is_method: bool
    is_dunder: bool
    class_name: str | None = None  # immediate enclosing class, if any
    base_classes: tuple[str, ...] = ()  # textual base-class names of that class, e.g. ("ModelAdmin",)


@dataclass
class FileAnalysis:
    path: str
    tree: ast.AST | None
    error: str | None = None


# ---------------------------------------------------------------------------
# File discovery + parsing
# ---------------------------------------------------------------------------


def _iter_python_files(
    root: str, exclude_dirs: set[str], exclude_files: set[str],
) -> Iterator[str]:
    for dirpath, dirnames, filenames in os.walk(root):
        dirnames[:] = [d for d in dirnames if d not in exclude_dirs]
        for filename in filenames:
            if not filename.endswith(".py"):
                continue
            if any(fnmatch(filename, pattern) for pattern in exclude_files):
                continue
            yield os.path.join(dirpath, filename)


_FALLBACK_ENCODINGS = ("utf-8-sig", "utf-16", "cp1252", "latin-1")


def _read_source(path: str) -> str:
    """
    Read a Python source file, tolerating a leading UTF-8 BOM (common from
    Windows editors) and a few other common encodings. "utf-8-sig" handles
    plain utf-8 files exactly like "utf-8" but additionally strips a leading
    U+FEFF if present, so it's tried first and covers the common case alone.
    """
    last_exc: Exception | None = None
    for encoding in _FALLBACK_ENCODINGS:
        try:
            with open(path, encoding=encoding) as fh:
                return fh.read()
        except (UnicodeDecodeError, UnicodeError) as exc:
            last_exc = exc
            continue
    raise last_exc  # type: ignore[misc]


def _parse_file(path: str) -> FileAnalysis:
    """Parse a single file. Module-level so it is picklable for multiprocessing."""
    try:
        source = _read_source(path)
        tree = ast.parse(source, filename=path)
        return FileAnalysis(path=path, tree=tree)
    except (SyntaxError, UnicodeDecodeError, UnicodeError, OSError) as exc:
        return FileAnalysis(path=path, tree=None, error=str(exc))


