# Directories that are essentially never worth scanning. Always excluded,
# on top of anything passed via --exclude-dir.
DEFAULT_EXCLUDE_DIRS = {
    ".git",
    "__pycache__",
    ".venv",
    "venv",
    "env",
    "node_modules",
    "build",
    "dist",
    ".tox",
    ".mypy_cache",
    ".pytest_cache",
    ".ruff_cache",
    "site-packages",
    ".eggs",
}


# ---------------------------------------------------------------------------
# Convention-based defaults (toggle off with --no-default-ignores)
#
# These are whole files, directories, and method names that follow common
# pytest/unittest/Django/Hypothesis conventions where the function is invoked
# by a framework via reflection or dynamic name lookup rather than a normal
# Name/Attribute reference - so a static reference count will essentially
# always read as 0 even though the code is very much in use. Each entry below
# notes the specific mechanism so it's clear why static analysis can't see it.
# ---------------------------------------------------------------------------

# Directory names skipped entirely.
DEFAULT_IGNORE_DIR_NAMES = {
    # Django migrations are autogenerated/historical and aren't meant to be
    # edited for "unused code" cleanup regardless of reference count; their
    # RunPython callables are also sometimes only wired up via string-like
    # operation arguments rather than a plain Name reference.
    "migrations",
}

# Filenames (fnmatch-style globs) skipped entirely, regardless of directory.
DEFAULT_IGNORE_FILENAME_PATTERNS = {
    # pytest/unittest discover these by name pattern, not by reference -
    # every test function in them will always read as "0 uses". This also
    # covers Hypothesis tests, since Hypothesis just adds @given on top of
    # ordinarily-named test_*.py files/functions.
    "test_*.py",
    "*_test.py",
    "tests.py",  # Django's single-file-per-app test module convention
    # pytest auto-discovers and imports conftest.py implicitly; its fixtures
    # are consumed by parameter-name injection in test files that may live
    # in unrelated directories, so it's prone to false positives even with
    # the parameter-name handling in ReferenceCounter.
    "conftest.py",
    # Django's AppConfig.ready() (and similar hooks) are invoked by the app
    # registry via attribute-name convention, not a direct call site.
    "apps.py",
    # ModelAdmin methods (e.g. custom list_display columns) are very commonly
    # wired up as *string* values in class attributes like list_display,
    # readonly_fields, etc. - a string literal, not a Name/Attribute node.
    "admin.py",
    # Signal receivers are typically only referenced via the @receiver(...)
    # decorator (which references the decorator, not the decorated function)
    # or connected by Django internally - rarely called directly by name.
    "signals.py",
}

# Method/function name regexes ignored everywhere, regardless of file -
# all of these are invoked by a framework via name convention.
DEFAULT_IGNORE_NAME_PATTERNS = (
    r"^test_",  # pytest / unittest / Hypothesis test discovery
    r"^(setUp|tearDown|setUpClass|tearDownClass|setUpTestData|setUpModule|tearDownModule)$",  # unittest/Django TestCase lifecycle hooks
    r"^clean_",  # Django form/model per-field validators: clean_<field>
    r"^clean$",  # Django form/model clean()
    r"^validate_",  # DRF serializer per-field validators: validate_<field>
    r"^validate$",  # DRF serializer validate()
    r"^get_.*_display$",  # Django model get_FOO_display()
    r"^(get|post|put|patch|delete|head|options|trace)$",  # Django class-based view HTTP verb dispatch (View.dispatch uses getattr(self, request.method.lower()))
    r"^(list|retrieve|create|update|partial_update|destroy)$",  # DRF ViewSet actions, routed by the router rather than called directly
    r"^ready$",  # Django AppConfig.ready()
    r"^handle$",  # Django management Command.handle()
    r"^add_arguments$",  # Django management Command.add_arguments()
    r"^(visit|leave)_",  # ast.NodeVisitor/NodeTransformer dispatch via getattr(self, "visit_" + ...)
)


