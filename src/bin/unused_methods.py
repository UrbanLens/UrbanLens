class _PlainProgressBar:
    def __init__(self, total: int, desc: str, enabled: bool):
        self.total = total
        self.desc = desc
        self.enabled = enabled and total > 0
        self.count = 0
        if self.enabled:
            self._render()

    def _render(self) -> None:
        pct = (self.count / self.total) * 100 if self.total else 100
        print(f"\r{self.desc}: {self.count}/{self.total} ({pct:5.1f}%)", end="", file=sys.stderr)

    def update(self, n: int = 1) -> None:
        self.count += n
        if self.enabled:
            self._render()

    def close(self) -> None:
        if self.enabled:
            print(file=sys.stderr)


def _make_progress_bar(total: int, desc: str, enabled: bool):
    if enabled and _HAS_TQDM:
        return tqdm(total=total, desc=desc, unit="file")
    return _PlainProgressBar(total, desc, enabled)


