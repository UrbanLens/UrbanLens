def _parse_args(argv: list[str] | None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("directory", nargs="?", default=".", help="Directory to scan (default: current directory)")
    parser.add_argument("-o", "--output", default="unused_functions.txt", help="Report file path")
    parser.add_argument("--json", action="store_true", help="Write the report as JSON instead of text")
    parser.add_argument("--exclude-dir", action="append", default=[], metavar="NAME", help="Extra directory name to skip (repeatable)")
    parser.add_argument("--exclude-file", action="append", default=[], metavar="GLOB", help="Filename glob to skip, e.g. queryset.py (repeatable)")
    parser.add_argument("--include-dunder", action="store_true", help="Include __dunder__ methods in the report")
    parser.add_argument(
        "--ignore-pattern",
        action="append",
        default=[],
        metavar="REGEX",
        help="Regex (matched against qualified or simple name) to exclude from "
        "the report, repeatable. Use for naming-convention dispatch that's "
        "invisible to static analysis, e.g. ast.NodeVisitor's visit_* methods: "
        "--ignore-pattern '^visit_|^leave_'",
    )
    parser.add_argument(
        "--no-default-ignores",
        action="store_true",
        help="Disable the built-in convention-based ignores (pytest/unittest/Hypothesis "
        "test files, conftest.py, Django migrations/apps.py/admin.py/signals.py, and "
        "method names like test_*, clean_*, get/post/.../ready/handle that are invoked "
        "via framework reflection rather than a normal reference). On by default.",
    )
    parser.add_argument(
        "--no-string-literal-refs",
        action="store_true",
        help="Don't treat a string literal that exactly matches a function/method name "
        "as a use. By default these count, to catch dispatch-by-string patterns like "
        "Django/DRF's as_view({\"get\": \"method_name\"}) and getattr(obj, \"name\"). "
        "Disable for a stricter, reference-only count (risk: more false positives for "
        "those patterns, but no risk of an unrelated string coincidentally matching a "
        "generically-named function).",
    )
    parser.add_argument(
        "--min-confidence",
        choices=["low", "medium", "high"],
        default="low",
        help="Only include results at or above this confidence level (default: low = include everything)",
    )
    parser.add_argument("--min-uses", type=int, default=1, help="Reference count at/under which a def is reported as 'rare' (default: 1)")
    parser.add_argument("--workers", type=int, default=1, help="Parallel worker processes for parsing (default: 1)")
    parser.add_argument("--no-progress", action="store_true", help="Disable the progress bar")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = _parse_args(argv)

    results = find_unused_functions(
        args.directory,
        exclude_dirs=set(args.exclude_dir),
        exclude_files=set(args.exclude_file),
        include_dunder=args.include_dunder,
        min_uses=args.min_uses,
        ignore_patterns=args.ignore_pattern,
        use_default_ignores=not args.no_default_ignores,
        match_string_literals=not args.no_string_literal_refs,
        workers=args.workers,
        show_progress=not args.no_progress,
    )

    if args.min_confidence != "low":
        rank = {"Low": 0, "Medium": 1, "High": 2}
        threshold = {"low": 0, "medium": 1, "high": 2}[args.min_confidence]
        results = {key: [f for f in findings if rank[f.confidence] >= threshold] for key, findings in results.items()}

    if args.json:
        write_json_report(results, args.output)
    else:
        write_text_report(results, args.output)

    print(
        f"Done. {len(results['unused'])} unused, {len(results['rare'])} rarely-used. "
        f"Report written to {args.output}",
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
